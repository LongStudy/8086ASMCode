		MOV BX,[SI]
		INC SI
		INC SI
		CMP AX,BX
		JZ L4
		MOV [DI],BX
		INC DI
		INC DI
		JMP L5
L4:		
        DEC BUF
L5:
		LOOP L3
		
OK:
		MOV AH,4CH
		INT 21H
		
